    private void hitung_jumlah_bayarActionPerformed(java.awt.event.ActionEvent evt) {
        int JumlahBarang = Integer.parseInt(TOTAL_BARANG.getText());
        int HargaBarang = Integer.parseInt(HARGA.getText());
        TOTAL.setText(Integer.toString(HargaBarang * JumlahBarang));
    }
    
    private void PILIHActionPerformed(java.awt.event.ActionEvent evt) {
    String NamaBarang = (String) PILIH.getSelectedItem();
    
    // Membuat objek dari BarangFactory
    BarangFactory factory = new BarangFactory();
    
    // Memanggil metode getBarang melalui objek factory
    Barang barang = factory.getBarang(NamaBarang); 
    
    // Set harga barang yang sesuai
    HARGA.setText(Integer.toString(barang.getHarga()));
}

    private void batalActionPerformed(java.awt.event.ActionEvent evt) {
        HARGA.setText("");
        TOTAL_BARANG.setText("");
        TOTAL.setText("");
        JML_BAYAR.setText("");
        kembalian.setText("");
        PILIH.setSelectedIndex(0);
    }

