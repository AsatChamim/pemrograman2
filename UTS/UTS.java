    private void bayarActionPerformed(java.awt.event.ActionEvent evt) {
        try {
            int hargaTotal = Integer.parseInt(TOTAL.getText());
            int UangBayar = Integer.parseInt(JML_BAYAR.getText());
            if (UangBayar < hargaTotal) {
                JOptionPane.showMessageDialog(null, "Uang Anda Kurang :V", "SYSTEM NOTICE", JOptionPane.INFORMATION_MESSAGE);
            } else {
                kembalian.setText(Integer.toString(UangBayar - hargaTotal));
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Input angka yang valid", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    // Kelas Barang dan Factory harus non-statis
    class Barang {
        protected String nama;
        protected int harga;

        public Barang(String nama, int harga) {
            this.nama = nama;
            this.harga = harga;
        }

        public int getHarga() {
            return harga;
        }
    }

    class Buku extends Barang {
        public Buku() {
            super("Buku", 3000);
        }
    }

    class Bolpoin extends Barang {
        public Bolpoin() {
            super("Bolpoin", 2500);
        }
    }
    
    class Pensil extends Barang {
        public Pensil() {
            super("Pensil", 2000);
        }
    }

    class Penggaris extends Barang {
        public Penggaris() {
            super("Penggaris", 5000);
        }
    }
    
    class Penghapus extends Barang {
        public Penghapus() {
            super("Penghapus", 1000);
        }
    }

    class BarangFactory {
        Barang getBarang(String namaBarang) {
            switch (namaBarang) {
                case "BUKU":
                    return new Buku();
                case "BOLPOIN":
                    return new Bolpoin();
                case "PENSIL":
                    return new Pensil();
                case "PENGGARIS":
                    return new Penggaris();
                case "PENGHAPUS":
                    return new Penghapus();
                default:
                    return new Barang("Unknown", 0);
            }
        }
    }

